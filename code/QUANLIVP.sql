ALTER PROC PHONGBAN_INSERT
@TENPB NVARCHAR(50)
AS
BEGIN
	INSERT INTO PHONGBAN VALUES (@TENPB)

END

ALTER PROC PHONGBAN_UPDATE
@TENPB NVARCHAR(50),
@MA INT
AS
BEGIN
	UPDATE PHONGBAN SET TENPHONGBAN = @TENPB WHERE MAVP = @MA

END

ALTER PROC PHONGBAN_DELETE
@MA INT
AS
BEGIN
	DELETE FROM PHONGBAN WHERE MAVP = @MA

END
CREATE PROC PHONGBAN_SELECTALL
AS
BEGIN
	SELECT * FROM PHONGBAN
END


